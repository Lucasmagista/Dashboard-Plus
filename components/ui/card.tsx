import * as React from "react"
import { motion } from "framer-motion"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<typeof motion.div>
>(({ className, ...props }, ref) => (
  <motion.div
    ref={ref}
    className={cn(
      "rounded-lg border bg-card text-card-foreground shadow-sm bg-gradient-to-br from-[rgba(0,0,0,0.02)] to-[rgba(0,0,0,0.08)] dark:from-[rgba(255,255,255,0.02)] dark:to-[rgba(255,255,255,0.08)] transition-colors duration-500",
      className
    )}
    initial={{ opacity: 0, y: 20 }}
    animate={{ opacity: 1, y: 0 }}
    exit={{ opacity: 0, y: 20 }}
    transition={{ duration: 0.5 }}
    {...props}
  />
))
Card.displayName = "Card"

